#!/usr/bin/env node
// Regenerate src/utils/pg-keywords.ts from PostgreSQL's kwlist.h plus the
// PL/pgSQL reserved/unreserved kwlists.
//
// Pin to a specific stable branch so this is reproducible. Re-run when
// bumping the libpg-query major (which corresponds to a PostgreSQL major)
// to pick up newly reserved keywords.

import { writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const PG_BRANCH = "REL_17_STABLE";
const KWLIST_URL = `https://raw.githubusercontent.com/postgres/postgres/${PG_BRANCH}/src/include/parser/kwlist.h`;
const PL_RESERVED_URL = `https://raw.githubusercontent.com/postgres/postgres/${PG_BRANCH}/src/pl/plpgsql/src/pl_reserved_kwlist.h`;
const PL_UNRESERVED_URL = `https://raw.githubusercontent.com/postgres/postgres/${PG_BRANCH}/src/pl/plpgsql/src/pl_unreserved_kwlist.h`;

const __dirname = dirname(fileURLToPath(import.meta.url));
const outFile = join(__dirname, "..", "src", "utils", "pg-keywords.ts");

const fetchText = async (url) => {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Failed to fetch ${url}: ${res.status}`);
  return res.text();
};

const [sqlSource, plReservedSource, plUnreservedSource] = await Promise.all([
  fetchText(KWLIST_URL),
  fetchText(PL_RESERVED_URL),
  fetchText(PL_UNRESERVED_URL),
]);

const keywords = new Map();
for (const line of sqlSource.split("\n")) {
  // PG_KEYWORD("name", TOKEN_VALUE, CATEGORY, BARE_LABEL_STATUS)
  const match = line.match(
    /^PG_KEYWORD\("([^"]+)",\s*\w+,\s*(UNRESERVED_KEYWORD|COL_NAME_KEYWORD|TYPE_FUNC_NAME_KEYWORD|RESERVED_KEYWORD)/,
  );
  if (!match) continue;
  keywords.set(match[1], match[2]);
}

if (keywords.size === 0) {
  throw new Error("No keywords parsed; kwlist.h format may have changed.");
}

// PL/pgSQL keyword files use a 2-arg PG_KEYWORD("name", TOKEN). Collect
// both reserved and unreserved into one set; the case-folding rule does
// not need to distinguish — only "is this a keyword in a plpgsql body".
const plKeywords = new Set();
for (const source of [plReservedSource, plUnreservedSource]) {
  for (const line of source.split("\n")) {
    const match = line.match(/^PG_KEYWORD\("([^"]+)"/);
    if (match) plKeywords.add(match[1]);
  }
}

if (plKeywords.size === 0) {
  throw new Error(
    "No PL/pgSQL keywords parsed; pl_*_kwlist.h format may have changed.",
  );
}

// Record which PL/pgSQL keywords are reserved (cannot be identifier in
// PL/pgSQL) vs unreserved (can be identifier).
const plReservedSet = new Set();
for (const line of plReservedSource.split("\n")) {
  const m = line.match(/^PG_KEYWORD\("([^"]+)"/);
  if (m) plReservedSet.add(m[1]);
}

const sorted = [...keywords.entries()].sort(([a], [b]) => a.localeCompare(b));

const requiresQuotes = sorted
  .filter(([, c]) => c !== "UNRESERVED_KEYWORD")
  .map(([k]) => k);

// Keywords PostgreSQL refuses as identifiers in any position —
// `RESERVED_KEYWORD` in kwlist.h plus the PL/pgSQL reserved kwlist.
// Case-folding rules that walk source text without full positional
// context must only touch this set, because UNRESERVED / COL_NAME /
// TYPE_FUNC_NAME keywords (`role`, `date`, `value`, `name`, …) can
// legitimately be column / function names and case-folding them would
// corrupt the SQL. We intentionally omit the larger union of kwlist
// and PL/pgSQL kwlists — it has no current consumer and exporting it
// would invite future identifier-corruption regressions of the same
// shape.
const plpgsqlReserved = [
  ...new Set([
    ...sorted.filter(([, c]) => c === "RESERVED_KEYWORD").map(([k]) => k),
    ...plReservedSet,
  ]),
].sort();

const out =
  `// Auto-generated by scripts/generate-pg-keywords.js from ` +
  `PostgreSQL ${PG_BRANCH} kwlist.h, pl_reserved_kwlist.h,\n` +
  `// and pl_unreserved_kwlist.h. Do not edit by hand; re-run the script.\n\n` +
  `// Keywords that require double-quoting when used as an identifier in any\n` +
  `// context — everything categorized as RESERVED_KEYWORD,\n` +
  `// COL_NAME_KEYWORD, or TYPE_FUNC_NAME_KEYWORD in PostgreSQL's kwlist.h.\n` +
  `// COL_NAME_KEYWORD and TYPE_FUNC_NAME_KEYWORD each have positional\n` +
  `// restrictions (e.g. cannot be used as a type/function name, or as a\n` +
  `// column name, respectively); since lint rules visit identifier tokens\n` +
  `// without full positional context, treating these categories as "needs\n` +
  `// quoting" is the only cross-context-safe choice.\n` +
  `export const PG_KEYWORDS_REQUIRING_QUOTES: ReadonlySet<string> = new Set([\n` +
  requiresQuotes.map((k) => `  ${JSON.stringify(k)},`).join("\n") +
  `\n]);\n\n` +
  `// Every keyword that may appear in a PL/pgSQL function body — the union\n` +
  `// of the SQL kwlist and the PL/pgSQL reserved + unreserved kwlists. Used\n` +
  `// by case-folding rules that walk plpgsql bodies; lower-cased here so\n` +
  `// callers can compare with .toLowerCase() lookup.\n` +
  `// Keywords PostgreSQL refuses as identifiers in any position —\n` +
  `// \`RESERVED_KEYWORD\` from kwlist.h plus the PL/pgSQL reserved kwlist.\n` +
  `// Case-folding rules that walk source text without full positional\n` +
  `// context must only touch this set, because UNRESERVED / COL_NAME /\n` +
  `// TYPE_FUNC_NAME keywords (\`role\`, \`date\`, \`value\`, \`name\`, …) can\n` +
  `// legitimately be column / function names and case-folding them would\n` +
  `// corrupt the SQL.\n` +
  `export const PLPGSQL_RESERVED_KEYWORDS: ReadonlySet<string> = new Set([\n` +
  plpgsqlReserved.map((k) => `  ${JSON.stringify(k)},`).join("\n") +
  `\n]);\n`;

writeFileSync(outFile, out);
console.log(
  `Wrote ${keywords.size} kwlist keywords (${requiresQuotes.length} require quoting) and ${plpgsqlReserved.length} reserved plpgsql keywords to ${outFile}`,
);
